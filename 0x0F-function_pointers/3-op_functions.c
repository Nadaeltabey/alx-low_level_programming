#include "3-calc.h"
/**
 * op_add - writes th
 * @a: Th
 * @b: operand
 * Return: On success 1.
 * On error, -1 is returned, and errno is set appropriately.
 */
int op_add(int a, int b)
{
return (a + b);
}

